#ifndef LLVM_LIB_TARGET_FUNNYARCH_FUNNYARCHSUBTARGET_H
#define LLVM_LIB_TARGET_FUNNYARCH_FUNNYARCHSUBTARGET_H

#include "FunnyarchFrameLowering.h"
#include "FunnyarchISelLowering.h"
#include "FunnyarchInstrInfo.h"
#include "llvm/CodeGen/SelectionDAGTargetInfo.h"
#include "llvm/CodeGen/TargetSubtargetInfo.h"
#include "llvm/IR/DataLayout.h"
#include "llvm/MC/MCInstrItineraries.h"

#define GET_SUBTARGETINFO_HEADER
#include "FunnyarchGenSubtargetInfo.inc"

namespace llvm {
class FunnyarchTargetMachine;

class FunnyarchSubtarget : public FunnyarchGenSubtargetInfo {
protected:
  SelectionDAGTargetInfo TSInfo;
  FunnyarchInstrInfo InstrInfo;
  FunnyarchFrameLowering FrameLowering;
  FunnyarchTargetLowering TLInfo;
  FunnyarchRegisterInfo RegInfo;

public:
  /// This constructor initializes the data members to match that
  /// of the specified triple.
  FunnyarchSubtarget(const Triple &TT, StringRef CPU, StringRef FS, const FunnyarchTargetMachine &TM);

  /// ParseSubtargetFeatures - Parses features string setting specified
  /// subtarget options.  Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef TuneCPU, StringRef FS);

  FunnyarchSubtarget &initializeSubtargetDependencies(const Triple &TT,
                                                  StringRef CPU, StringRef TuneCPU,
                                                  StringRef FS);

  const SelectionDAGTargetInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }

  const FunnyarchInstrInfo *getInstrInfo() const override {
    return &InstrInfo;
  }

  const TargetFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }

  const FunnyarchRegisterInfo *getRegisterInfo() const override {
    return &RegInfo;
  }

  const FunnyarchTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }

  /// getMaxInlineSizeThreshold - Returns the maximum memset / memcpy size
  /// that still makes it profitable to inline the call.
  unsigned getMaxInlineSizeThreshold() const {
    return 64;
  }
};
}

#endif // end LLVM_LIB_TARGET_FUNNYARCH_FUNNYARCHSUBTARGET_H
